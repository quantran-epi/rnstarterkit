type ButtonColorVariant = "primary" | "success" | "danger" | "warning" | "transparent";
type ButtonSizeVariant = "lg" | "md" | "sm";
type ButtonShapeVariant = "square" | "rounded";

type ButtonVariant = ButtonColorVariant | ButtonSizeVariant | ButtonShapeVariant

export type {
    ButtonColorVariant,
    ButtonSizeVariant,
    ButtonShapeVariant,
    ButtonVariant
}